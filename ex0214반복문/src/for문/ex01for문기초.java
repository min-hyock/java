package for문;

public class ex01for문기초 {

	public static void main(String[] args) {

		// for문 구조
		// 1부터 10까지 출력
		for (int i = 1; i < 11; i++) {
			System.out.print(i+" ");
		}
		System.out.println();
		
		// 52부터 21까지 출력
		for(int i = 52; i > 20; i--) {
			System.out.print(i + " ");
		}
		
		
		
		
		
		
		
		
		
		
		
	}

}
